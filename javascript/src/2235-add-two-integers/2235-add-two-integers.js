/*
Description:

Given two integers num1 and num2, return the sum of the two integers.
 */

/*
 * @param {number} num1
 * @param {number} num2
 * @return {number}
 */

/*
Space complexity: O(1)
Time complexity: O(1)
 */

const sum = function (num1, num2) {
    return num1 + num2;
};

module.exports = {
    sum
}